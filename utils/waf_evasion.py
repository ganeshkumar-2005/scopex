"""
utils/waf_evasion.py — Adaptive WAF detection and evasion for ScopeX v2.

Detects WAF block signatures in HTTP responses and recommends evasion techniques.
Integrated into BaseScanner automatically through ctx.waf_evasion and
ctx.waf_evasion_profile, but can also be used standalone for explicit WAF
fingerprinting passes.
"""
from __future__ import annotations

import random
from dataclasses import dataclass, field
from enum import Enum
from typing import Dict, List, Optional, Tuple

import httpx
from loguru import logger


# ---------------------------------------------------------------------------
# Evasion action enum
# ---------------------------------------------------------------------------


class EvasionAction(Enum):
    """Recommended action after a WAF block is detected."""

    NONE = "none"
    """No block detected — proceed normally."""

    ROTATE_UA = "rotate_ua"
    """Rotate the User-Agent string and retry."""

    ADD_DELAY = "add_delay"
    """Add a longer randomised delay before retrying."""

    CHANGE_ENCODING = "change_encoding"
    """Apply URL / unicode encoding variants to the payload."""

    FRAGMENT_REQUEST = "fragment_request"
    """Split the request across multiple partial requests (chunked)."""

    ABORT = "abort"
    """Abort scanning this target — block count exceeded retry budget."""


